public class Person {
    //attributi
    public int personID;
    public int name;
    public int surname;
    public int email;
    public int password;

    //costruttore classe Person
    public Person(int personID, int name, int surname, int email, int password) {
        this.personID = personID;
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.password = password;
    }


}
